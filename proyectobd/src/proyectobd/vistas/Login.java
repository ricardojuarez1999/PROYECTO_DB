    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String contra = new String(this.jPFContra.getPassword());
        if (!this.jTFUser.getText().equals("") && !contra.equals("")) {
            int id = Integer.parseInt(this.jTFUser.getText());
            this.empleado = controladorEmpleado.getSesion(id, contra);

            if (this.empleado != null) {
                Horario horario = controladorHorario.obtenerHorario(this.empleado.getIdHorario());

                String[] partirHora = horario.getEntrada().split(":");
                int hora = Integer.parseInt(partirHora[0]);
                if (hora < 23) {
                    hora += 1;
                }
                String aux = String.valueOf(hora) + ":" + partirHora[1] + ":" + partirHora[2];
                horario.setEntrada(aux);

                Calendar calendario = Calendar.getInstance();
                String horaActual = calendario.get(Calendar.HOUR_OF_DAY) + ":" + calendario.get(Calendar.MINUTE) + ":" + calendario.get(Calendar.SECOND);

                try {
                    Asistencia asistencia;

                    asistencia = controladorAsistencia.obtenerPorFechayID(empleado.getIdEmpleado());
                    
                    if(asistencia != null){
                        horaActual = asistencia.getHora_entrada();
                        if(asistencia.getHora_salida() != null){
                            JOptionPane.showMessageDialog(this,"Ya has marcado salida vuelve el siguiente dia");
                            this.jTFUser.setText("");
                            this.jPFContra.setText("");
                            return;
                        }
                    }
                    DateFormat dateFormat = new SimpleDateFormat("hh:mm");

                    Date DHoraEntrada = dateFormat.parse(horario.getEntrada());
                    Date DHoraActual = dateFormat.parse(horaActual);
                    
                    

                    if (DHoraEntrada.compareTo(DHoraActual) > 0) {

                        if (asistencia == null) {
                            asistencia = new Asistencia();
                            asistencia.setIdEmpleado(this.empleado.getIdEmpleado());
                            controladorAsistencia.insertarAsistencia(asistencia);
                        }
                        switch (this.empleado.getIdPuesto()) {
                            case 1: // Vendedor
                                this.dispose();
                                Venta.info = this.empleado;
                                Venta venta = new Venta();
                                venta.setVisible(true);
                                break;
                            case 2: // Supervisor
                            case 3: //Jefe
                            case 4: //Gerente
                            case 5: // Contador
                                this.dispose();
                                Admin admin = new Admin(this.empleado);
                                admin.setVisible(true);
                                break;
                            case 6:
                                break;
                        }
                    } else {
                        try {
                            MySQLInasistencia myinasistencia = new MySQLInasistencia();

                            Inasistencia inasistencia = new MySQLInasistencia().obtenerIdyporFecha(this.empleado.getIdEmpleado());

                            if (inasistencia == null) {
                                inasistencia = new Inasistencia();
                                inasistencia.setIdEmpleado(this.empleado.getIdEmpleado());
                                inasistencia.setIdTipoInasistencia(2);
                                myinasistencia.insertar(inasistencia);
                            }
                            JOptionPane.showMessageDialog(this, "No se permite entrar despues de una hora tarde vuelve proximo dia");
                            System.exit(0);
                        } catch (Excepcion e) {
                            JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                            System.exit(0);
                        }
                    }
                } catch (ParseException ex) {
                    Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Usuario y contrase√±a es incorrecta / intente de nuevo", "Error", JOptionPane.ERROR_MESSAGE);
                this.jTFUser.setText("");
                this.jPFContra.setText("");
            }

        } else {
            if (this.jTFUser.getText().equals("") || this.jTFUser.getText().equals("0")) {
                this.jTFUser.setBorder(BorderFactory.createLineBorder(new Color(238, 112, 82), 1));
            }
            if (contra.equals("")) {
                this.jPFContra.setBorder(BorderFactory.createLineBorder(new Color(238, 112, 82), 1));
            }
            this.jTFUser.setText("");
            this.jPFContra.setText("");
            JOptionPane.showMessageDialog(null, "Ingresa los campos para iniciar sesion", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

