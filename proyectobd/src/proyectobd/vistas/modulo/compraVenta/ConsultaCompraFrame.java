    private void ConsultarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ConsultarMouseClicked
        MySQLFacturaGeneral auxFacturaGeneral = new MySQLFacturaGeneral();
        
        DefaultTableModel model = (DefaultTableModel)tabla.getModel();
        
        DiaMin.setEnabled(false);MesMin.setEnabled(false);AñoMin.setEnabled(false);
        DiaMax.setEnabled(false);MesMax.setEnabled(false);AñoMax.setEnabled(false);
        Producto.setEnabled(false);Servicio.setEnabled(false);Pieza.setEnabled(false);
        MontoMin.setEnabled(false);MontoMax.setEnabled(false);IdEmpleado.setEnabled(false);
        Efectivo.setEnabled(false);Tarjeta.setEnabled(false);Consultar.setEnabled(false);
        
        String fechaMin = "";
        String fechaMax = "";
        boolean producto = Producto.isSelected();
        boolean servicio = Servicio.isSelected();
        boolean pieza = Pieza.isSelected();
        double montoMin = 0.00;
        double montoMax = -1.00;
        int idEmpleado = 0;
        boolean efectivo = Efectivo.isSelected();
        boolean tarjeta = Tarjeta.isSelected();
        
        int indexProducto = 5;
        int indexPieza = 2;
        int indexServicio = 4;
        
        int indexEfectivo = 1;
        int indexTarjeta = 2;
        
        
        String diaMin = "01";
        String mesMin = "01"; 
        String añoMin = "1999";
        Calendar c = Calendar.getInstance();
        String mesMax = "" + (c.get(Calendar.MONTH) + 1);
        String diaMax = "" + c.get(Calendar.DAY_OF_MONTH);
        String añoMax = "" + c.get(Calendar.YEAR);
        if(MontoMin.getText().equals("")){
            montoMin = 0.00;
        }else{
            montoMin = Double.parseDouble(MontoMin.getText().replace(",", "."));
        }
        if(MontoMax.getText().equals("")){
            montoMax = -1.00;
        }else{
            montoMax = Double.parseDouble(MontoMax.getText().replace(",", "."));
        }
        if(IdEmpleado.getText().equals("")){
            idEmpleado = 0;
        }else{
            idEmpleado = Integer.parseInt(IdEmpleado.getText());
        }
        if(DiaMin.getSelectedIndex() != 0){diaMin = DiaMin.getSelectedItem().toString();}
        if(MesMin.getSelectedIndex() != 0){mesMin = "" + MesMin.getSelectedIndex();}
        if(AñoMin.getSelectedIndex() != 0){añoMin = AñoMin.getSelectedItem().toString();}
        fechaMin = añoMin.toString() +"-"+mesMin.toString()+"-"+diaMin.toString();
        if(DiaMax.getSelectedIndex() != 0){diaMax = DiaMax.getSelectedItem().toString();}
        if(MesMax.getSelectedIndex() != 0){mesMax = "" + MesMax.getSelectedIndex();}
        if(AñoMax.getSelectedIndex() != 0){añoMax = AñoMax.getSelectedItem().toString();}
        fechaMax = añoMax +"-"+mesMax+"-"+diaMax;
        ArrayList <FacturaGeneral> aux = new ArrayList();
        
        ArrayList <FacturaGeneral> listaFacturaGeneral = auxFacturaGeneral.getByDate(fechaMin, fechaMax);
        
        for(int i = 0; i<listaFacturaGeneral.size();i++){
            if(producto){
                if(listaFacturaGeneral.get(i).getIdTipoFactura() == indexProducto){
                    aux.add(listaFacturaGeneral.get(i));
                }
            }
            if(servicio){
                if(listaFacturaGeneral.get(i).getIdTipoFactura() == indexServicio){
                    aux.add(listaFacturaGeneral.get(i));
                }
            }
            if(pieza){
                if(listaFacturaGeneral.get(i).getIdTipoFactura() == indexPieza){
                    aux.add(listaFacturaGeneral.get(i));
                }
            }
        }
        
        listaFacturaGeneral = new ArrayList();
        
        if(montoMax != -1.00){
            for(int i = 0; i<aux.size();i++){
                if((aux.get(i).getTotal() >=  montoMin) &&(aux.get(i).getTotal() <=  montoMax) ){
                    listaFacturaGeneral.add(aux.get(i));
                }
            }
        }else{
            for(int i = 0; i<aux.size();i++){
                listaFacturaGeneral.add(aux.get(i));
            }
        }
        aux = new ArrayList();
        
        if(idEmpleado != 0){
            for(int i = 0; i<listaFacturaGeneral.size();i++){
                if(listaFacturaGeneral.get(i).getIdEmpleado() == idEmpleado){
                    aux.add(listaFacturaGeneral.get(i));
                }
            }
        }else{
            for(int i = 0; i<listaFacturaGeneral.size();i++){
                aux.add(listaFacturaGeneral.get(i));
            }
        }
        
        listaFacturaGeneral = new ArrayList();
        
        for(int i = 0; i<aux.size();i++){
            if(efectivo){
                if(aux.get(i).getIdTipoPago() == indexEfectivo){
                    listaFacturaGeneral.add(aux.get(i));
                }
            }
            if(tarjeta){
                if(aux.get(i).getIdTipoPago() == indexTarjeta){
                    listaFacturaGeneral.add(aux.get(i));
                }
            } 
        }
        if(listaFacturaGeneral.isEmpty()){
            Object[] columnas = {"N/A","N/A","N/A","N/A","N/A","N/A","N/A",};
            model.addRow(columnas);
        }else{
            for(int i = 0; i<listaFacturaGeneral.size();i++){
                Object[] columnas = {listaFacturaGeneral.get(i).getIdFacturaGeneral(),listaFacturaGeneral.get(i).getFecha(),listaFacturaGeneral.get(i).getIdEmpleado(),listaFacturaGeneral.get(i).getIdEmpresa(),listaFacturaGeneral.get(i).getIdTipoPago(),listaFacturaGeneral.get(i).getIdTipoFactura(),listaFacturaGeneral.get(i).getTotal()};
                model.addRow(columnas);
            }
        }
    }//GEN-LAST:event_ConsultarMouseClicked

