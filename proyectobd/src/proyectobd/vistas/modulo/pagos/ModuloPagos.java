    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (!this.jTFIdPago.getText().equals("")) {
            int idPago = Integer.parseInt(this.jTFIdPago.getText());

            if (!this.jTFIdEmpleado.getText().equals("")) {
                int id = Integer.parseInt(this.jTFIdEmpleado.getText());

                Empleado emp = this.controladorEmpleado.buscarEmpleado(id);

                if (emp != null) {
                    Calendar c = Calendar.getInstance();
                    int dia = c.get(Calendar.DATE);
                    int mes = c.get(Calendar.MONTH);
                    int annio = c.get(Calendar.YEAR);

                    ArrayList<Pago> lista = new MySQLPago().listarPagosDeEmpleado(annio, emp.getIdEmpleado());

                    boolean b = false;
                    if (!lista.isEmpty()) {
                        for (int i = 0; i < lista.size(); i++) {
                            if (lista.get(i).getIdPago() == idPago) {
                                b = true;
                                Pago pag = new MySQLPago().obtenerId(idPago);
                                if (pag != null) {
                                    ArrayList<DetallePago> listaDetalle = new MySQLDetallePago().listarPorIDPago(pag.getIdPago());
                                    if (!listaDetalle.isEmpty()) {
                                        this.jTFIdPago.setText("");
                                        DetallePag detPago;
                                        detPago = new DetallePag(listaDetalle);
                                        detPago.setVisible(true);

                                    } else {
                                        JOptionPane.showMessageDialog(this, "EL ID no tiene detalle");
                                        this.jTFIdPago.setText("");
                                    }
                                }
                                break;
                            }
                        }
                        if (!b) {
                            JOptionPane.showMessageDialog(this, "No existe el ID de pago para este empleado");
                        }
                    }

                }
            } else {
                JOptionPane.showMessageDialog(this, "Ingrese el id del empleado");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Ingrese el ID Pago para visualizar sus detalles");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

