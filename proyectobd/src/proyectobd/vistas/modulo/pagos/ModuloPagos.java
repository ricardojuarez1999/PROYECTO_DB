    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        ArrayList<Empleado> listaEmpleados = controladorEmpleado.listarEmpleados();

        if (listaEmpleados != null) {
            for (int i = 0; i < listaEmpleados.size(); i++) {
                float total = 0;
                ArrayList<DetallePago> listaDetallePago = new ArrayList();

                DetallePago det = new DetallePago();
                det.setDetalle("Sueldo");
                det.setMonto(controladorPuesto.buscarPuesto(listaEmpleados.get(i).getIdPuesto()).getSueldo());
                listaDetallePago.add(det);

                total += det.getMonto();

                Calendar c = Calendar.getInstance();
                int dia = c.get(Calendar.DATE);
                int mes = c.get(Calendar.MONTH);
                int annio = c.get(Calendar.YEAR);

                if (listaEmpleados.get(i).getIdPuesto() == 1) {

                    int comiciones = new MySQLFacturaGeneral().listarComiciones(mes + 1, annio, listaEmpleados.get(i).getIdEmpleado());

                    DetallePago nuevoDet = new DetallePago();
                    nuevoDet.setDetalle("Comiciones");
                    nuevoDet.setMonto((float) (comiciones * 2));
                    total += nuevoDet.getMonto();
                    listaDetallePago.add(nuevoDet);
                }
                int numInasistencia = new MySQLInasistencia().numeroInasistencias(mes + 1, annio, listaEmpleados.get(i).getIdEmpleado());
                float descuento = -80 * numInasistencia;
                DetallePago x = new DetallePago();
                x.setDetalle("Inasistencias");
                x.setMonto(descuento);
                listaDetallePago.add(x);

                total += descuento;

                System.out.println("ID: " + listaEmpleados.get(i).getIdEmpleado() + " Total: " + total);

                int idMov = new MySQLConsultas().obtenerUltimoID("movimiento_financiero");

                idMov--;
                MovimientoFinanciero mov = null;
                MySQLMovimientoFinanciero sqlmov = new MySQLMovimientoFinanciero();

                mov = sqlmov.obtenerId(idMov);

                if (mov != null) {
                    mov.setIdTipoTransaccion(2);
                    mov.setMonto(total);

                    if (mov.getMonto() < mov.getTotal()) {
                        mov.setTotal(mov.getTotal() - total);
                        sqlmov.insertarFechaActual(mov);
                    } else {
                        JOptionPane.showMessageDialog(this, "No tienes los recursos para realizar el pago");
                        return;
                    }

                }

                int idPago = new MySQLConsultas().obtenerUltimoID("pago");

                MySQLPago mysqlPago = new MySQLPago();

                Pago pagoNuevo = new Pago();
                pagoNuevo.setIdEmpleado(listaEmpleados.get(i).getIdEmpleado());
                pagoNuevo.setPago(total);

                mysqlPago.insertar(pagoNuevo);

                pagoNuevo = new MySQLPago().obtenerId(idPago);

                for (int j = 0; j < listaDetallePago.size(); j++) {
                    listaDetallePago.get(j).setIdPago(pagoNuevo.getIdPago());
                    MySQLDetallePago mysqlDetallePago = new MySQLDetallePago();
                    mysqlDetallePago.insertar(listaDetallePago.get(j));
                }
            }
            JOptionPane.showMessageDialog(this, "Se realizaron los pagos correctamente");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

