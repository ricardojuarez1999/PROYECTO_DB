    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        int cont = this.jTable1.getRowCount();
        for (int i = cont - 1; i >= 0; i--) {
            modelo.removeRow(i);
        }

        if (!this.jTFIdEmpleado.getText().equals("")) {
            int id = Integer.parseInt(this.jTFIdEmpleado.getText());

            Empleado emp = this.controladorEmpleado.buscarEmpleado(id);

            if (emp != null) {
                Calendar c = Calendar.getInstance();
                int dia = c.get(Calendar.DATE);
                int mes = c.get(Calendar.MONTH);
                int annio = c.get(Calendar.YEAR);

                ArrayList<Pago> lista = new MySQLPago().listarPagosDeEmpleado(annio, emp.getIdEmpleado());
                if (!lista.isEmpty()) {
                    for (int i = 0; i < lista.size(); i++) {
                        String[] valores = new String[4];
                        valores[0] = String.valueOf(lista.get(i).getIdPago());
                        valores[1] = String.valueOf(lista.get(i).getPago());

                        long f = lista.get(i).getFecha().getTime();
                        valores[2] = String.valueOf(new java.sql.Date(f));
                        valores[3] = String.valueOf(lista.get(i).getIdEmpleado());

                        modelo.addRow(valores);
                        this.jTFIdPago.setText("");
                        this.jTFIdPago.setEnabled(true);
                    }
                }

            }
        } else {
            JOptionPane.showMessageDialog(this, "Ingrese el id del empleado");
        }
    }//GEN-LAST:event_jButton8ActionPerformed

