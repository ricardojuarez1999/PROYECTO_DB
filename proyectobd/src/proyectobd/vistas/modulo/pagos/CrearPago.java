    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        if (!this.jTFIdEmpleado.getText().equals("")) {

            float total = 0;
            for (int i = 0; i < this.listaDetalle.size(); i++) {
                total += this.listaDetalle.get(i).getMonto();
            }

            int idMov = new MySQLConsultas().obtenerUltimoID("movimiento_financiero");

            idMov--;
            MovimientoFinanciero mov = null;
            MySQLMovimientoFinanciero sqlmov = new MySQLMovimientoFinanciero();

            mov = sqlmov.obtenerId(idMov);

            if (mov != null) {
                mov.setIdTipoTransaccion(2);
                mov.setMonto(total);

                if (mov.getMonto() < mov.getTotal()) {
                    mov.setTotal(mov.getTotal() - total);
                    sqlmov.insertarFechaActual(mov);
                } else {
                    JOptionPane.showMessageDialog(this, "No tienes los recursos para realizar el pago");
                    return;
                }

            }

            int idPago = new MySQLConsultas().obtenerUltimoID("pago");

            Pago pago = new Pago();
            pago.setPago(total);
            pago.setIdEmpleado(Integer.parseInt(this.jTFIdEmpleado.getText()));

            MySQLPago mysqlPago = new MySQLPago();

            mysqlPago.insertar(pago);

            pago = mysqlPago.obtenerId(idPago);

            for (int i = 0; i < this.listaDetalle.size(); i++) {
                listaDetalle.get(i).setIdPago(pago.getIdPago());
                MySQLDetallePago mysqlDetallePago = new MySQLDetallePago();
                mysqlDetallePago.insertar(listaDetalle.get(i));
            }

            JOptionPane.showMessageDialog(this, "Se realizo el pago correctamente");
        } else {
            JOptionPane.showMessageDialog(this, "Llena todos los campos");
        }    // TODO add your handling code here:
    }//GEN-LAST:event_jButton9ActionPerformed

