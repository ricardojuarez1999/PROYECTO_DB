    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        if (!this.jTFIdEmpleado.getText().equals("") && !this.jTFMonto.getText().equals("") && !this.jTFDetalle.getText().equals("")) {
            Empleado emp = new MySQLEmpleado().obtenerId(Integer.parseInt(this.jTFIdEmpleado.getText()));

            if (emp != null) {
                DetallePago detPago = new DetallePago();
                detPago.setMonto(Float.parseFloat(this.jTFMonto.getText()));
                detPago.setDetalle(this.jTFDetalle.getText());

                listaDetalle.add(detPago);
                JOptionPane.showMessageDialog(this, "Se agrego correctamente");
                this.jTFDetalle.setText("");
                this.jTFMonto.setText("");
                this.jTFIdEmpleado.setEnabled(false);
            } else {
                JOptionPane.showMessageDialog(this, "El ID del empleado no existe");
            }

        } else {
            JOptionPane.showMessageDialog(this, "Llena todos los campos");
        }
    }//GEN-LAST:event_jButton10ActionPerformed

